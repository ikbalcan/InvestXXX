def create_portfolio_recommendations_export(recommendations, portfolio, export_date=None):
    """Portf√∂y √∂nerilerini Word formatƒ±nda export eder"""
    
    if export_date is None:
        export_date = datetime.now()
    
    doc = Document()
    
    # Ba≈ülƒ±k
    title = doc.add_heading('ü§ñ Robot Portf√∂y Y√∂neticisi - G√ºnl√ºk √ñneriler Raporu', 0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    # Tarih
    doc.add_paragraph(f'üìÖ Rapor Tarihi: {export_date.strftime("%d.%m.%Y %H:%M")}')
    doc.add_paragraph()
    
    # Portf√∂y √ñzeti
    doc.add_heading('üìä Portf√∂y √ñzeti', level=1)
    
    total_cash = portfolio.get('cash', 0)
    portfolio_value = sum([s['quantity'] * s['avg_cost'] for s in portfolio.get('stocks', {}).values()])
    total_capital = total_cash + portfolio_value
    
    portfolio_table = doc.add_table(rows=4, cols=2)
    portfolio_table.style = 'Table Grid'
    
    portfolio_data = [
        ('üíµ Toplam Nakit', f'{total_cash:,.2f} TL'),
        ('üìä Portf√∂y Deƒüeri', f'{portfolio_value:,.2f} TL'),
        ('üí∞ Toplam Sermaye', f'{total_capital:,.2f} TL'),
        ('üìà Hisse Sayƒ±sƒ±', f'{len(portfolio.get("stocks", {}))} adet')
    ]
    
    for i, (label, value) in enumerate(portfolio_data):
        portfolio_table.rows[i].cells[0].text = label
        portfolio_table.rows[i].cells[1].text = value
    
    doc.add_paragraph()
    
    # ƒ∞≈ülem √∂nerilerini grupla
    sell_actions = [r for r in recommendations if r['action'] in ['SAT', 'KISMEN SAT']]
    buy_actions = [r for r in recommendations if r['action'] == 'YENƒ∞ AL']
    increase_actions = [r for r in recommendations if r['action'] == 'ARTIR']
    hold_actions = [r for r in recommendations if r['action'] == 'TUT']
    
    # 1. SATI≈û √ñNERƒ∞LERƒ∞
    if sell_actions:
        doc.add_heading('üî¥ 1. SATI≈û √ñNERƒ∞LERƒ∞', level=1)
        
        total_sell_value = sum([r['recommended_value'] for r in sell_actions])
        doc.add_paragraph(f'Toplam Satƒ±≈ü Deƒüeri: {total_sell_value:,.2f} TL')
        doc.add_paragraph()
        
        for idx, rec in enumerate(sell_actions, 1):
            doc.add_heading(f'{idx}. {rec["symbol"]}', level=2)
            
            # ƒ∞≈ülem bilgileri
            doc.add_paragraph(f'√ñnerilen ƒ∞≈ülem: {rec["action"]}')
            doc.add_paragraph(f'Miktar: {rec["recommended_quantity"]:.0f} adet')
            doc.add_paragraph(f'ƒ∞≈ülem Tutarƒ±: {rec["recommended_value"]:,.2f} TL')
            doc.add_paragraph(f'G√ºncel Fiyat: {rec["current_price"]:,.2f} TL')
            
            if rec.get('avg_cost'):
                profit_loss = rec.get('profit_loss', 0)
                profit_loss_pct = rec.get('profit_loss_pct', 0)
                doc.add_paragraph(f'Kar/Zarar: {profit_loss:+,.2f} TL ({profit_loss_pct:+.1f}%)')
            
            if rec.get('action_reason'):
                doc.add_paragraph(f'üí° Sebep: {rec["action_reason"]}')
            
            doc.add_paragraph()
    
    # 2. YENƒ∞ ALIM √ñNERƒ∞LERƒ∞
    if buy_actions:
        doc.add_heading('üü¢ 2. YENƒ∞ ALIM √ñNERƒ∞LERƒ∞', level=1)
        
        total_buy_value = sum([r['recommended_value'] for r in buy_actions])
        doc.add_paragraph(f'Toplam Alƒ±m Deƒüeri: {total_buy_value:,.2f} TL')
        doc.add_paragraph()
        
        for idx, rec in enumerate(buy_actions, 1):
            doc.add_heading(f'{idx}. {rec["symbol"]}', level=2)
            
            # ƒ∞≈ülem bilgileri
            doc.add_paragraph(f'√ñnerilen Miktar: {rec["recommended_quantity"]:.0f} adet')
            doc.add_paragraph(f'G√ºncel Fiyat: {rec["current_price"]:,.2f} TL')
            doc.add_paragraph(f'ƒ∞≈ülem Tutarƒ±: {rec["recommended_value"]:,.2f} TL')
            
            if rec.get('target_price'):
                potential_return = ((rec['target_price'] - rec['current_price']) / rec['current_price']) * 100
                doc.add_paragraph(f'Hedef Fiyat: {rec["target_price"]:,.2f} TL (Potansiyel Getiri: %{potential_return:+.1f})')
            
            if rec.get('confidence'):
                doc.add_paragraph(f'G√ºven Skoru: %{rec["confidence"]*100:.0f}')
            
            # Neden AL dediƒüinin √∂zeti
            if rec.get('action_reason'):
                doc.add_paragraph(f'üí° Ana Sebep: {rec["action_reason"]}')
            
            # Detaylƒ± sebepler
            doc.add_paragraph('üìã Neden AL √ñnerisi:')
            reasons = generate_buy_reasons(rec)
            for reason in reasons:
                doc.add_paragraph(f'  ‚Ä¢ {reason}', style='List Bullet')
            
            doc.add_paragraph()
    
    # 3. POZƒ∞SYON ARTIRIMLARI
    if increase_actions:
        doc.add_heading('üìà 3. POZƒ∞SYON ARTIRIMLARI', level=1)
        
        for idx, rec in enumerate(increase_actions, 1):
            doc.add_heading(f'{idx}. {rec["symbol"]}', level=2)
            
            doc.add_paragraph(f'Mevcut Pozisyon: {rec["quantity"]:.0f} adet')
            doc.add_paragraph(f'Ek √ñnerilen: {rec["recommended_quantity"]:.0f} adet')
            doc.add_paragraph(f'Toplam Olacak: {rec["quantity"] + rec["recommended_quantity"]:.0f} adet')
            doc.add_paragraph(f'ƒ∞≈ülem Tutarƒ±: {rec["recommended_value"]:,.2f} TL')
            
            if rec.get('action_reason'):
                doc.add_paragraph(f'üí° Sebep: {rec["action_reason"]}')
            
            # Neden ARTIR dediƒüinin √∂zeti
            doc.add_paragraph('üìã Neden ARTIR √ñnerisi:')
            reasons = generate_increase_reasons(rec)
            for reason in reasons:
                doc.add_paragraph(f'  ‚Ä¢ {reason}', style='List Bullet')
            
            doc.add_paragraph()
    
    # 4. TAKƒ∞P EDƒ∞LECEK Hƒ∞SSELER
    if hold_actions:
        doc.add_heading('üü° 4. TAKƒ∞P EDƒ∞LECEK Hƒ∞SSELER', level=1)
        
        for idx, rec in enumerate(hold_actions, 1):
            doc.add_heading(f'{idx}. {rec["symbol"]}', level=2)
            
            doc.add_paragraph(f'Mevcut Pozisyon: {rec["quantity"]:.0f} adet')
            doc.add_paragraph(f'G√ºncel Fiyat: {rec.get("current_price", 0):,.2f} TL')
            
            if rec.get('avg_cost'):
                profit_loss = rec.get('profit_loss', 0)
                profit_loss_pct = rec.get('profit_loss_pct', 0)
                doc.add_paragraph(f'Kar/Zarar: {profit_loss:+,.2f} TL ({profit_loss_pct:+.1f}%)')
            
            if rec.get('action_reason'):
                doc.add_paragraph(f'üí° Durum: {rec["action_reason"]}')
            
            doc.add_paragraph()
    
    # ƒ∞≈ûLEM √ñZETƒ∞
    doc.add_heading('üìä ƒ∞≈ülem √ñzeti', level=1)
    
    total_buy = sum([r['recommended_value'] for r in buy_actions + increase_actions])
    total_sell = sum([r['recommended_value'] for r in sell_actions])
    net_cash = total_cash + total_sell - total_buy
    
    summary_table = doc.add_table(rows=5, cols=2)
    summary_table.style = 'Table Grid'
    
    summary_data = [
        ('üìä Portf√∂y Deƒüeri', f'{portfolio_value:,.2f} TL'),
        ('üíµ Ba≈ülangƒ±√ß Nakit', f'{total_cash:,.2f} TL'),
        ('üí∞ Toplam Satƒ±≈ü', f'+{total_sell:,.2f} TL'),
        ('üí∏ Toplam Alƒ±m', f'-{total_buy:,.2f} TL'),
        ('üíµ Kalan Nakit', f'{net_cash:,.2f} TL')
    ]
    
    for i, (label, value) in enumerate(summary_data):
        summary_table.rows[i].cells[0].text = label
        summary_table.rows[i].cells[1].text = value
    
    doc.add_paragraph()
    
    # Footer
    doc.add_paragraph('_' * 50)
    doc.add_paragraph(f'Rapor Olu≈üturulma Tarihi: {export_date.strftime("%d.%m.%Y %H:%M")}')
    doc.add_paragraph('ü§ñ Robot Portf√∂y Y√∂neticisi - AI Destekli Yatƒ±rƒ±m √ñnerileri')
    doc.add_paragraph()
    doc.add_paragraph('‚ö†Ô∏è UYARI: Bu √∂neriler yatƒ±rƒ±m tavsiyesi niteliƒüi ta≈üƒ±maz. Yatƒ±rƒ±m kararlarƒ±nƒ±zƒ± kendi ara≈ütƒ±rmanƒ±zƒ± yaparak alƒ±n.')
    
    return doc


def generate_buy_reasons(rec):
    """AL √∂nerisi i√ßin sebepler listesi olu≈üturur"""
    reasons = []
    
    # Model tahmini
    confidence = rec.get('confidence', 0)
    prediction = rec.get('prediction')
    
    if prediction == 1 and confidence > 0.6:
        reasons.append(f'G√º√ßl√º AI model sinyali (%{confidence*100:.0f} g√ºven)')
    elif prediction == 1 and confidence > 0.5:
        reasons.append(f'Orta seviye AI model sinyali (%{confidence*100:.0f} g√ºven)')
    
    # Teknik analiz sinyalleri
    result = rec.get('result', {})
    if result:
        rsi = result.get('rsi', 50)
        if rsi < 30:
            reasons.append(f'RSI a≈üƒ±rƒ± satƒ±m seviyesinde ({rsi:.1f}) - Y√ºkseli≈ü fƒ±rsatƒ±')
        elif rsi < 40:
            reasons.append(f'RSI d√º≈ü√ºk seviyede ({rsi:.1f}) - Potansiyel y√ºkseli≈ü')
        
        trend = result.get('trend_strength', '')
        if trend == 'Y√ºkseli≈ü':
            reasons.append('Fiyat hareketli ortalamalarƒ±n √ºzerinde - G√º√ßl√º trend')
        
        volume_ratio = result.get('volume_ratio', 1.0)
        if volume_ratio > 1.5:
            reasons.append(f'Hacim ortalamanƒ±n {volume_ratio:.1f}x √ºzerinde - ƒ∞lgi artƒ±≈üƒ±')
        
        price_change_1w = result.get('price_change_1w', 0)
        if price_change_1w > 3:
            reasons.append(f'Son 1 haftada %{price_change_1w:.1f} y√ºkseli≈ü - Momentum devam ediyor')
    
    # Hedef fiyat
    target_price = rec.get('target_price')
    current_price = rec.get('current_price', 0)
    if target_price and current_price:
        potential_return = ((target_price - current_price) / current_price) * 100
        if potential_return > 10:
            reasons.append(f'Potansiyel getiri %{potential_return:.1f} - Y√ºksek kazan√ß fƒ±rsatƒ±')
        elif potential_return > 5:
            reasons.append(f'Potansiyel getiri %{potential_return:.1f} - ƒ∞yi kazan√ß fƒ±rsatƒ±')
    
    # Portf√∂y tahsisi
    allocation = rec.get('allocation_pct', 0)
    if allocation > 0:
        reasons.append(f'Portf√∂y tahsisi %{allocation*100:.0f} - Risk daƒüƒ±lƒ±mƒ±na uygun')
    
    # Eƒüer hi√ß sebep yoksa genel mesaj
    if not reasons:
        reasons.append('AI modeli pozitif sinyal veriyor')
    
    return reasons[:5]  # En fazla 5 sebep


def generate_increase_reasons(rec):
    """ARTIR √∂nerisi i√ßin sebepler listesi olu≈üturur"""
    reasons = []
    
    confidence = rec.get('confidence', 0)
    if confidence > 0.6:
        reasons.append(f'G√º√ßl√º y√ºkseli≈ü sinyali (%{confidence*100:.0f} g√ºven)')
    
    result = rec.get('result', {})
    if result:
        rsi = result.get('rsi', 50)
        if rsi < 30:
            reasons.append('A≈üƒ±rƒ± satƒ±m b√∂lgesinde - ƒ∞yi fƒ±rsat')
        
        trend = result.get('trend_strength', '')
        if trend == 'Y√ºkseli≈ü':
            reasons.append('Mevcut trend g√º√ßl√º - Artƒ±rƒ±m mantƒ±klƒ±')
    
    profit_loss_pct = rec.get('profit_loss_pct', 0)
    if profit_loss_pct > -3:
        reasons.append('Mevcut pozisyon zararda deƒüil - Artƒ±rƒ±m g√ºvenli')
    
    return reasons[:4]  # En fazla 4 sebep

